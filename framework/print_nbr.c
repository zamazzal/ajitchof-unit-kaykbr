void	print_nbr(int n)
{
	if (n < 0)
	{
		ft_putchar(45);
		if (n == -2147483648)
		{
			ft_putnbr(214748364);
			ft_putchar('8');
			return ;
		}
		else
			n = -n;
	}
	if (n >= 10)
	{
		ft_putnbr(n / 10);
		ft_putnbr(n % 10);
	}
	else
		ft_putchar(n + 48);
}
